package jeu;

import java.util.ConcurrentModificationException;
import java.util.Iterator;
import java.util.NoSuchElementException;

import cartes.Carte;

public class Sabot implements Iterable<Carte>{
	
	private Carte[] cartes;
	private int nbCartes;
	private int nbOperations = 0;
	Iterator<Carte> iterateur = iterator();

	public Sabot(Carte[] cartes) {
		this.cartes = cartes;
		this.nbCartes = cartes.length;
	}
	
	public boolean estVide() {
		return this.nbCartes == 0;
	}
	
	public void ajouterCarte(Carte carte) {
		if(nbCartes < cartes.length) {
			cartes[nbCartes] = carte;
			nbCartes++;
		} else {
			throw new IllegalStateException("Dépassement de capacité");			
		}
		nbOperations++;
	}
	
	public Carte piocher() {
		Carte carte = iterateur.next();
		iterateur.remove();
		return carte;
	}
	
	// Demander si "iterator" doit etre private, car il ne peut y avoir qu'un seul itérateur
	// et il sert de pioche
	@Override
	public Iterator<Carte> iterator() {
		return new Iterateur();
	}
	
	private class Iterateur implements Iterator<Carte>{
		private int indiceIterateur = 0;
		private boolean nextEffectue = false;
		private int nbOperationReference = 0;
		
		@Override
		public boolean hasNext() {
			return indiceIterateur < nbCartes;
		}
		
		@Override
		public Carte next() {
			verificationConcurrence();
			if(hasNext()) {
				Carte carte = cartes[indiceIterateur];
				indiceIterateur++;
				nextEffectue = true;
				return carte;
			} else throw new NoSuchElementException();
		}
		
		@Override
		public void remove() {
			verificationConcurrence();
			if (nbCartes < 1 || !nextEffectue) {
				throw new IllegalStateException();
			}
			for(int i = indiceIterateur - 1; i < nbCartes - 1; i++) {
				cartes[i] = cartes[i+1];
			}
			nextEffectue = false;
			indiceIterateur--;
			nbCartes--;
			nbOperationReference++;
			nbOperations++;
		}
		
		private void verificationConcurrence() {
			if(nbOperationReference != nbOperations)
				throw new ConcurrentModificationException();
		}
	}
}





